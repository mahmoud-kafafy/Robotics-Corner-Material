#include <iostream>
using namespace std;

int main() {
    int a = 10;           // Integer
    float b = 3.14f;      // Float
    double c = 2.7182818; // Double precision
    char d = 'A';         // Character
    bool e = true;        // Boolean

    cout << a << ", " << b << ", " << c << ", " << d << ", " << e << "\n";
    return 0;
}
